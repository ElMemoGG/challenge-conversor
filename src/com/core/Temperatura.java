package com.core;

/**
 *
 * @author Memo
 */
public class Temperatura {
    
    private int type;
    private double grados;

    public Temperatura(int type, double grados) {
        this.type = type;
        this.grados = grados;
    }
    
   
    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public double getGrados() {
        return grados;
    }

    public void setGrados(double grados) {
        this.grados = grados;
    }

    
    
    
    
    
}
